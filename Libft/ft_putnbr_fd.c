#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	num;

	if (n == INT_MIN)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	num = (n % 10) + 48;
	ft_putchar_fd(num, fd);
}
